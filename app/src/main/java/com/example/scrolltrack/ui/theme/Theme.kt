@Composable
fun ScrollTrackTheme(
    darkThemeUserPreference: Boolean = true, // Parameter to allow forcing dark for preview, defaults to true
    dynamicColor: Boolean = true,      // Allow dynamic color based on wallpaper (Android 12+)
    content: @Composable () -> Unit
) {
    val systemIsDark = isSystemInDarkTheme()
    // If user preference is to force dark, use dark. Otherwise, respect system theme for dynamic color.
    val useDarkTheme = darkThemeUserPreference || systemIsDark

    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (useDarkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        useDarkTheme -> ForcedDarkColorScheme
        else -> DefaultLightColorScheme // Fallback to defined light scheme if not dynamic and not forced dark
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        shapes = Shapes, // Add this line
        content = content
    )
}
